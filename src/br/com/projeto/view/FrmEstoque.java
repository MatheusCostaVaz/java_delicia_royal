    private void tblProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMouseClicked
        idproduto = Integer.parseInt(tblProdutos.getValueAt(tblProdutos.getSelectedRow(), 0).toString());
        txtNomeProduto.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(), 1).toString());
        txtQtdAtual.setText(tblProdutos.getValueAt(tblProdutos.getSelectedRow(), 4).toString());
        
    }//GEN-LAST:event_tblProdutosMouseClicked

    private void btnRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverMouseClicked
try {
           int qtd_estoque, qtd;
           qtd_estoque = Integer.parseInt(txtQtdAtual.getText());
           qtd = Integer.parseInt(txtQtdNovo.getText());
           
           qtd_nova = qtd_estoque - qtd;
           
           ProdutosDAO dao = new ProdutosDAO();
           
           dao.estoque(idproduto, qtd_nova);
           
           JOptionPane.showMessageDialog(null,"Estoque removido!");
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Erro:"+e);
            

        }
        
        new Utilitarios().LimpaTela(painelProdutos);        
    }//GEN-LAST:event_btnRemoverMouseClicked

