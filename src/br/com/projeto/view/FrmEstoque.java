    private void btnAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdicionarMouseClicked
        try {
           int qtd_estoque, qtd;
           qtd_estoque = Integer.parseInt(txtQtdAtual.getText());
           qtd = Integer.parseInt(txtQtdNovo.getText());
           
           qtd_nova = qtd_estoque + qtd;
           
           ProdutosDAO dao = new ProdutosDAO();
           
           dao.estoque(idproduto, qtd_nova);
           
           JOptionPane.showMessageDialog(null,"Estoque adicionado!");
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Erro:"+e);
            

        }
        
        new Utilitarios().LimpaTela(painelProdutos);
    }//GEN-LAST:event_btnAdicionarMouseClicked

