    public void listar() {

        MercadoriaDAO dao = new MercadoriaDAO();
        List<Mercadoria> lista = dao.listarMercadoria();
        DefaultTableModel dados = (DefaultTableModel) tabelaMateriais.getModel();
        dados.setNumRows(0);

        for (Mercadoria c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getQtd_estoque(),
                c.getTipo(),
                c.getPreco(),                
                c.getDescricao()
            });
        }
    }

