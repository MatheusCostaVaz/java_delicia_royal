    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Botão de excluir

        Mercadoria obj = new Mercadoria();

        obj.setId(Integer.parseInt(txtCodigo.getText()));

        MercadoriaDAO dao = new MercadoriaDAO();
        dao.excluirMercadoria(obj);
        new Utilitarios().LimpaTela(painelMateriais);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Salvar no banco de dados

        Mercadoria obj = new Mercadoria();
        obj.setNome(txtNome.getText());
        obj.setDescricao(txtDescricao.getText());
        obj.setQtd_estoque(Integer.parseInt(txtQtd.getText()));
        obj.setTipo(txtTipo.getText());
        obj.setPreco(Double.parseDouble(txtPreco.getText()));
        
        MercadoriaDAO dao = new MercadoriaDAO();
        dao.cadastrarMercadoria(obj);

        new Utilitarios().LimpaTela(painelMateriais);
    }//GEN-LAST:event_btnSalvarActionPerformed

