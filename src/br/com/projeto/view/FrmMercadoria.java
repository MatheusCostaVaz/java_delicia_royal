    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //Botão Editar
        Mercadoria obj = new Mercadoria();
        obj.setNome(txtNome.getText());
        obj.setDescricao(txtDescricao.getText());
        obj.setTipo(txtTipo.getText());
        obj.setQtd_estoque(Integer.parseInt(txtQtd.getText()));
        obj.setPreco(Double.parseDouble(txtPreco.getText()));

        obj.setId(Integer.parseInt(txtCodigo.getText()));

        MercadoriaDAO dao = new MercadoriaDAO();
        dao.alterarMercadoria(obj);

        new Utilitarios().LimpaTela(painelMateriais);
    }//GEN-LAST:event_btnEditarActionPerformed

